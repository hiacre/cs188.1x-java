package util;

/**
 *
 * @author archie
 */
public class PositionGrid {
    private final int x;
    private final int y;
    
    public PositionGrid(final int x, final int y) {
        this.x = x;
        this.y = x;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
}
