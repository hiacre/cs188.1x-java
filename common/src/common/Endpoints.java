package common;

/**
 *
 * @author archie
 */
public class Endpoints {

    final double first;
    final double second;
    
    public Endpoints(double first, double second) {
        this.first = first;
        this.second = second;
    }
    
    public double getFirst() {
        return first;
    }
    
    public double getSecond() {
        return second;
    }
}
